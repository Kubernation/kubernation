    #[test]
    fn redact_masks_credentials_not_plain_text() {
        assert_eq!(redact("password=hunter2 ok"), "password=•••• ok");
        assert_eq!(redact("token=abc.def"), "token=••••");
        assert_eq!(redact("db_password=x"), "db_password=••••");
        assert_eq!(redact("api-key=x"), "api-key=••••");
        // key: value (colon) + the Bearer / Authorization header shapes.
        assert_eq!(redact("password: hunter2"), "password: ••••");
        assert_eq!(redact("token: abc rotated"), "token: •••• rotated");
        assert_eq!(redact("Bearer eyJhbGci"), "Bearer ••••");
        assert_eq!(
            redact("Authorization: Bearer eyJhbGci done"),
            "Authorization: Bearer •••• done"
        );
        // URL basic-auth, authority only — the port is kept.
        assert_eq!(
            redact("dsn=postgres://user:secretpw@db:5432/x"),
            "dsn=postgres://user:••••@db:5432/x"
        );

        // No false positives / no data loss on plain operator text.
        assert_eq!(
            redact("BackOff: back-off restarting failed container"),
            "BackOff: back-off restarting failed container"
        );
        assert_eq!(
            redact("Scaled up replica set web-7 to 5"),
            "Scaled up replica set web-7 to 5"
        );
        // Prose that merely mentions a credential word is untouched.
        assert_eq!(
            redact("token expired, rotate it"),
            "token expired, rotate it"
        );
        // #4: a key that only ends with a cred substring (no `_`/`-` boundary).
        assert_eq!(redact("mysecret=plainvalue"), "mysecret=plainvalue");
        // #1: a URL with an `@` in the path/query keeps the whole path intact.
        assert_eq!(
            redact("url=postgres://db:5432/x?owner=foo@bar.com"),
            "url=postgres://db:5432/x?owner=foo@bar.com"
        );
    }

